"""
数独ソルバー（バックトラッキング法）
====================================
停止性の証明はこのファイル末尾のコメントを参照。
"""

from typing import Optional


def find_empty(board: list[list[int]]) -> Optional[tuple[int, int]]:
    """空マス（0）を左上から順に探す。なければ None を返す。"""
    for r in range(9):
        for c in range(9):
            if board[r][c] == 0:
                return (r, c)
    return None


def is_valid(board: list[list[int]], row: int, col: int, num: int) -> bool:
    """(row, col) に num を置いても数独のルールに違反しないか検査する。"""
    # 行チェック
    if num in board[row]:
        return False
    # 列チェック
    if num in [board[r][col] for r in range(9)]:
        return False
    # 3×3 ボックスチェック
    box_r, box_c = (row // 3) * 3, (col // 3) * 3
    for r in range(box_r, box_r + 3):
        for c in range(box_c, box_c + 3):
            if board[r][c] == num:
                return False
    return True


def solve(board: list[list[int]]) -> bool:
    """
    バックトラッキングで数独を解く（in-place）。

    解が存在すれば True を返し board を更新する。
    存在しなければ False を返し board を元の状態に戻す。
    """
    cell = find_empty(board)
    if cell is None:
        # 空マスなし → 全マスが埋まった（解が見つかった）
        return True

    row, col = cell
    for num in range(1, 10):           # 候補: 1〜9
        if is_valid(board, row, col, num):
            board[row][col] = num      # 仮置き
            if solve(board):           # 再帰
                return True
            board[row][col] = 0        # バックトラック（元に戻す）

    return False                       # どの数字も置けない → 失敗


def print_board(board: list[list[int]]) -> None:
    """盤面を見やすく表示する。"""
    for r in range(9):
        if r % 3 == 0 and r != 0:
            print("------+-------+------")
        row_str = ""
        for c in range(9):
            if c % 3 == 0 and c != 0:
                row_str += "| "
            row_str += (str(board[r][c]) if board[r][c] != 0 else ".") + " "
        print(row_str)


