if __name__ == "__main__":
    puzzle = [
        [5, 3, 0,  0, 7, 0,  0, 0, 0],
        [6, 0, 0,  1, 9, 5,  0, 0, 0],
        [0, 9, 8,  0, 0, 0,  0, 6, 0],

        [8, 0, 0,  0, 6, 0,  0, 0, 3],
        [4, 0, 0,  8, 0, 3,  0, 0, 1],
        [7, 0, 0,  0, 2, 0,  0, 0, 6],

        [0, 6, 0,  0, 0, 0,  2, 8, 0],
        [0, 0, 0,  4, 1, 9,  0, 0, 5],
        [0, 0, 0,  0, 8, 0,  0, 7, 9],
    ]

    print("=== 問題 ===")
    print_board(puzzle)

    if solve(puzzle):
        print("\n=== 解答 ===")
        print_board(puzzle)
    else:
        print("\n解なし")


# ===========================================================================
# 停止性の証明
# ===========================================================================
#
# 【定理】solve(board) は有限回の再帰呼び出しで必ず終了する。
#
# 【証明】
#
# ▍1. 位置付け
#   アルゴリズムは「バックトラッキング付き深さ優先探索」であり、
#   その探索空間は有限の木（探索木）をなす。
#   探索木が有限であれば、深さ優先探索は必ず終了する。
#
# ▍2. 探索木の有限性
#   以下の「停止指標」Φ を定義する：
#
#       Φ(board) = 盤面上の空マス（値が 0 のセル）の個数
#
#   - 初期状態：Φ ∈ {0, 1, …, 81}（有限値）
#   - 再帰呼び出し前：board[row][col] = num（0 でない数を代入）
#       → Φ が必ず 1 減少する。
#   - 基底ケース：Φ = 0（空マスなし）→ 即座に True を返す（再帰なし）。
#
#   よって再帰の深さは高々 81 であり、探索木の深さは有限である。
#
# ▍3. 各ノードの分岐数の有限性
#   各空マスに対して試す候補は {1, …, 9} の 9 個以下である（有限）。
#   したがって探索木の分岐数も有限であり、
#   探索木全体のノード数 ≤ 9^81（有限）となる。
#
# ▍4. 各ノードの処理が停止すること
#   find_empty : 9×9 = 81 セルの走査 → O(81) で停止。
#   is_valid   : 行・列・ボックス各 9 要素の走査 → O(27) で停止。
#   いずれも無限ループを含まない。
#
# ▍5. 結論
#   ・探索木のノード数が有限（≤ 9^81）
#   ・各ノードでの処理が有限時間で停止
#   ・バックトラック時に board[row][col] = 0 で状態を完全に復元する
#     （同じ状態を再訪しない ── Φ の単調減少が保証）
#
#   以上より、solve(board) はいかなる入力に対しても有限ステップで停止する。
#   □（証明終）
#
# 【補足：探索木のサイズの上界について】
#   最悪ケースの上界は 9^81 ≈ 1.97×10^77 だが、
#   is_valid による枝刈りにより実際の探索空間は劇的に小さくなる。
#   典型的な問題では数百〜数万ノードで解が求まる。
